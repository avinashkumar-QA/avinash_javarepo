package keywords;

public class Vowel_Found {
	
	public static void main(String[] args) {
		
		
		char input= 'a';
		
		switch(input) {
		
		
		case 'a':
		case 'e':
		case 'i':
		case 'o':
		case 'u':
			
			
		System.out.println(input + " ----> is a vowel ");
		break;
		
		default :
			
		System.out.println(input + " ----> is a constant ");
		
		}
		
	}

}

package mathpackage;

public class Math_Class_Arithmatic {

	public static void main(String[] args) {

		System.out.println(Math.addExact(500, 600));
		System.out.println(Math.subtractExact(600, 100));
		System.out.println(Math.multiplyExact(5, 4));
		System.out.println(Math.floorDiv(20, 5));
		System.out.println(Math.max(900, 700));
		System.out.println(Math.floorMod(30, 5));
		System.out.println(Math.PI);
		System.out.println(Math.addExact(3455555555l, 5566666660l));
		System.out.println(Math.abs(-800));
		System.out.println(Math.random());
		// second way
		int result = Math.addExact(500, 6);
		System.out.println(result);

	}

}

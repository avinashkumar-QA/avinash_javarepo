package keywords;

public class Vowel_If_else {
	
	
	public static void main(String[] args) {
		
		
		char input='a';
		
		if(input=='a' || input == 'e' || input=='i' || input=='o' || input=='u')	
			
		System.out.println(input +  " ----> is a vowel ");
		
		
		else {
			
			
		System.out.println(input + " ----> is a constant ");
	
		
		}
		
	}
	
	

}

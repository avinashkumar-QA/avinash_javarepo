package Variable;

public class Global_Variable_DefaultValue {
	
	static double db;
	static boolean bn;
	static byte bt;
	static short sh;
	static long lg;
	static float ft;
	static char ch;
	static String name;
	
	
	
	

	public static void main(String[] args) {
		
		name= "ram";
		System.out.println("The result for double is ----> "   + db );
		System.out.println("The result for boolean is ----> "   + bn );
		System.out.println("The result for byte is ----> "   + bt );
		System.out.println("The result for short is ----> "   + sh );
		System.out.println("The result for long is ----> "   + lg );
		System.out.println("The result for float is ----> "   + ft );
		System.out.println("The result for char is ----> "   + ch );
		System.out.println("The result for String is ----> "   + name );
		
		

	}

}

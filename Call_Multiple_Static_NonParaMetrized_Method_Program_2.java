package practice.javaprogram;

public class Call_Multiple_Static_NonParaMetrized_Method_Program_2 {



	static void method1() {

		System.out.println("I'm method First");
	}

	static void method2() {

		System.out.println("I'm method Second");
	}

	static void method3() {

		System.out.println("I'm method Third");
	}

	static void method4() {

		System.out.println("I'm method Four");
	}

	static void method5() {

		System.out.println("I'm method Five");
	}

	public static void main(String[] args) {

		Call_Multiple_Static_NonParaMetrized_Method_Program_2 obj= new Call_Multiple_Static_NonParaMetrized_Method_Program_2();
		obj.method1();
		obj.method2();
		obj.method3();
		obj.method4();
		obj.method5();
		

	}

}
